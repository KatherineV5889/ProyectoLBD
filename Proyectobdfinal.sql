CREATE OR REPLACE PROCEDURE actualizar_ventas (
    p_id_categoria IN NUMBER,
    p_incremento IN NUMBER
) AS
BEGIN
    UPDATE ventas
    SET total = total + (total * p_incremento / 100)
    WHERE id_producto IN (
        SELECT id_producto 
        FROM productos 
        WHERE id_categoria = p_id_categoria
    );
    COMMIT;
END;

