SELECT 
    fecha, 
    SUM(cantidad) AS total_cantidad_ventas,
    SUM(total) AS total_monto_ventas
FROM 
    ventas
GROUP BY 
    fecha
HAVING 
    SUM(cantidad) = (
        SELECT MAX(SUM(cantidad))
        FROM ventas
        GROUP BY fecha
    );

